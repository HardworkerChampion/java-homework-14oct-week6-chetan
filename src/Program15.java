// Write a Java program to swap two variables.

public class Program15 {

    public static void main(String[] args) {

        String X = "water";
        String Y = "soda";
        String Temp;

        Temp = X;
        X = Y;
        Y = Temp;

        System.out.println("X:  " + X);
        System.out.println("Y;  " + Y);


    }
}
